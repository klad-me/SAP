#ifndef SAPVM_TYPES_H
#define SAPVM_TYPES_H


// Целое 8 бит со знаком
typedef signed char	sap_byte;
typedef unsigned char	sap_ubyte;

// Целое 16 бит знаковое
typedef signed short	sap_short;
typedef unsigned short	sap_ushort;

// Целое 32 бита знаковое
typedef signed int	sap_int;
typedef unsigned int	sap_uint;

// Вещественное 32 бита
typedef float		sap_float;


// Размер адреса для платформы
typedef sap_int		sap_address;


#endif
